from app.core.logging import get_logger

logger = get_logger(__name__)


class FallbackResponder:
    """
    Возвращает заготовленные ответы, если LLM недоступен.
    """

    TEMPLATES = {
        "pricing": "Привет! ✨ Сейчас не могу рассчитать точную стоимость, но минималка у нас от 3000₽. Давай я передам твой вопрос Софии, она скоро ответит!",
        "booking": "Привет! 🗓️ Чтобы записаться, мне нужно уточнить пару деталей у Софии. Передам ей твой контакт, она напишет в течение часа!",
        "health": "Это очень важный вопрос! Я передам его лично мастеру, она свяжется с тобой в ближайшее время 💛",
        "complaint": "Мне очень жаль, что так вышло 🙏 Я немедленно передала твой диалог Софии, она лично разберется в ситуации!",
        "ambiguous": "Привет! 👋 У нас сейчас большой поток сообщений, но я уже передала твой вопрос администратору. Ответим очень скоро!",
    }

    @classmethod
    def get_response(cls, intent: str) -> str:
        logger.warning("fallback_response_used", intent=intent)
        return cls.TEMPLATES.get(intent, cls.TEMPLATES["ambiguous"])
