async def seed_knowledge_base():
    """Наполнить БЗ начальными данными."""
    async with async_session_factory() as session:
        # Проверяем, не пустая ли уже БЗ
        result = await session.execute(select(KnowledgeBase))
        existing = result.scalars().all()

        if existing:
            logger.info("knowledge_base_not_empty", count=len(existing))
            print(f"⚠️  В БЗ уже есть {len(existing)} записей. Seed пропущен.")
            print(
                "   Чтобы очистить: python -c 'from app.domain.knowledge.models import KnowledgeBase; ...'"
            )
            return

        # Добавляем seed-данные
        for item in SEED_DATA:
            kb = KnowledgeBase(**item)
            session.add(kb)

        await session.commit()

        logger.info("knowledge_base_seeded", count=len(SEED_DATA))
        print(f"✅ Добавлено {len(SEED_DATA)} FAQ-записей в базу знаний!")


if __name__ == "__main__":
    asyncio.run(seed_knowledge_base())
